// 1 2 3 4
var number = 4;

switch (number) {
  case 1:
    console.log("you have selected 1");
    break;

  case 2:
    console.log("you have selected 2");
    break;

  case 3:
    console.log("you have selected 3");
    break;

  case 4:
    console.log("you have selected 4");
    break;

  default:
    console.log("you have not selected a number");
    break;
}
